/**
 * AIOS Entity Registry Activator
 * 
 * Populates the IDS Entity Registry with our custom squads and agents.
 * This enables the full IDS system (search, dependencies, health checks)
 * for all 101 agents across 16 squads.
 * 
 * Usage: node scripts/activate-registry.js
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const AIOS_ROOT = path.resolve(__dirname, '..');
const SQUADS_DIR = path.join(AIOS_ROOT, 'squads');
const REGISTRY_PATH = path.join(AIOS_ROOT, '.aios-core', 'data', 'entity-registry.yaml');

function sha256(filePath) {
    try {
        const content = fs.readFileSync(filePath);
        return 'sha256:' + crypto.createHash('sha256').update(content).digest('hex');
    } catch { return null; }
}

function extractKeywords(content) {
    const keywords = [];
    // Extract from kpi_thresholds
    const kpiMatch = content.match(/metric:\s*"([^"]+)"/g);
    if (kpiMatch) {
        kpiMatch.forEach(m => {
            const val = m.match(/metric:\s*"([^"]+)"/)[1];
            keywords.push(...val.toLowerCase().split(/\s+/).filter(w => w.length > 3));
        });
    }
    // Extract from title/role
    const titleMatch = content.match(/title:\s*(.+)/);
    if (titleMatch) {
        keywords.push(...titleMatch[1].toLowerCase().split(/\s+/).filter(w => w.length > 3));
    }
    return [...new Set(keywords)].slice(0, 10);
}

function extractPurpose(content) {
    const match = content.match(/whenToUse:\s*\|?\s*\n?\s*(.*)/);
    if (match) return match[1].trim().substring(0, 200);
    const titleMatch = content.match(/title:\s*(.+)/);
    if (titleMatch) return titleMatch[1].trim();
    return 'Custom AIOS agent';
}

function scanSquads() {
    const entities = { agents: {}, squads: {} };

    const squads = fs.readdirSync(SQUADS_DIR).filter(s => {
        try { return fs.statSync(path.join(SQUADS_DIR, s)).isDirectory(); }
        catch { return false; }
    });

    squads.forEach(squad => {
        const squadDir = path.join(SQUADS_DIR, squad);
        const yamlPath = path.join(squadDir, 'squad.yaml');

        // Register squad
        if (fs.existsSync(yamlPath)) {
            const content = fs.readFileSync(yamlPath, 'utf8');
            entities.squads[squad] = {
                path: `squads/${squad}/squad.yaml`,
                type: 'squad',
                purpose: `Squad: ${squad}`,
                keywords: [squad, 'squad'],
                usedBy: [],
                dependencies: [],
                adaptability: { score: 0.5, constraints: ['structure'], extensionPoints: ['agents'] },
                checksum: sha256(yamlPath),
                lastVerified: new Date().toISOString(),
            };
        }

        // Register agents
        const agentsDir = path.join(squadDir, 'agents');
        try {
            const agents = fs.readdirSync(agentsDir).filter(f => f.endsWith('.md'));
            agents.forEach(agentFile => {
                const agentId = agentFile.replace('.md', '');
                const agentPath = path.join(agentsDir, agentFile);
                const content = fs.readFileSync(agentPath, 'utf8');

                entities.agents[agentId] = {
                    path: `squads/${squad}/agents/${agentFile}`,
                    type: 'agent',
                    purpose: extractPurpose(content),
                    keywords: [...extractKeywords(content), squad],
                    usedBy: [],
                    dependencies: [squad],
                    adaptability: { score: 0.3, constraints: ['persona', 'kpi_thresholds'], extensionPoints: ['commands', 'skill_chains'] },
                    checksum: sha256(agentPath),
                    lastVerified: new Date().toISOString(),
                };

                // Add reverse dependency
                if (entities.squads[squad]) {
                    entities.squads[squad].usedBy.push(agentId);
                }
            });
        } catch { }
    });

    return entities;
}

function generateRegistryYaml(entities) {
    const agentCount = Object.keys(entities.agents).length;
    const squadCount = Object.keys(entities.squads).length;
    const total = agentCount + squadCount;

    let yaml = `# AIOS Custom Entity Registry\n`;
    yaml += `# Auto-generated by activate-registry.js\n`;
    yaml += `# DO NOT EDIT MANUALLY\n\n`;
    yaml += `metadata:\n`;
    yaml += `  version: "1.0.0"\n`;
    yaml += `  lastUpdated: "${new Date().toISOString()}"\n`;
    yaml += `  entityCount: ${total}\n`;
    yaml += `  checksumAlgorithm: "sha256"\n`;
    yaml += `  customSquads: ${squadCount}\n`;
    yaml += `  customAgents: ${agentCount}\n\n`;

    yaml += `entities:\n`;

    // Squads
    yaml += `  squads:\n`;
    Object.entries(entities.squads).forEach(([id, e]) => {
        yaml += `    ${id}:\n`;
        yaml += `      path: "${e.path}"\n`;
        yaml += `      type: "${e.type}"\n`;
        yaml += `      purpose: "${e.purpose}"\n`;
        yaml += `      keywords: [${e.keywords.map(k => `"${k}"`).join(', ')}]\n`;
        yaml += `      usedBy: [${e.usedBy.map(k => `"${k}"`).join(', ')}]\n`;
        yaml += `      dependencies: []\n`;
        yaml += `      checksum: "${e.checksum || 'pending'}"\n`;
        yaml += `      lastVerified: "${e.lastVerified}"\n`;
    });

    // Agents
    yaml += `  agents:\n`;
    Object.entries(entities.agents).forEach(([id, e]) => {
        yaml += `    ${id}:\n`;
        yaml += `      path: "${e.path}"\n`;
        yaml += `      type: "${e.type}"\n`;
        yaml += `      purpose: "${e.purpose.replace(/"/g, "'")}"\n`;
        yaml += `      keywords: [${e.keywords.map(k => `"${k}"`).join(', ')}]\n`;
        yaml += `      usedBy: []\n`;
        yaml += `      dependencies: [${e.dependencies.map(k => `"${k}"`).join(', ')}]\n`;
        yaml += `      checksum: "${e.checksum || 'pending'}"\n`;
        yaml += `      lastVerified: "${e.lastVerified}"\n`;
    });

    return yaml;
}

// Execute
console.log('🔍 Scanning custom squads...');
const entities = scanSquads();
const agentCount = Object.keys(entities.agents).length;
const squadCount = Object.keys(entities.squads).length;

console.log(`\n📊 Found:`);
console.log(`   🏢 ${squadCount} squads`);
console.log(`   🤖 ${agentCount} agents`);

const yaml = generateRegistryYaml(entities);

// Write to data directory
const dataDir = path.join(AIOS_ROOT, '.aios-core', 'data');
if (!fs.existsSync(dataDir)) fs.mkdirSync(dataDir, { recursive: true });

const outPath = path.join(dataDir, 'custom-entity-registry.yaml');
fs.writeFileSync(outPath, yaml, 'utf8');
console.log(`\n✅ Registry written to: .aios-core/data/custom-entity-registry.yaml`);
console.log(`   Total entities: ${agentCount + squadCount}`);

// Also write a JSON version for programmatic access
const jsonPath = path.join(dataDir, 'custom-entity-registry.json');
fs.writeFileSync(jsonPath, JSON.stringify({
    metadata: {
        version: '1.0.0',
        lastUpdated: new Date().toISOString(),
        entityCount: agentCount + squadCount,
        customSquads: squadCount,
        customAgents: agentCount,
    },
    entities
}, null, 2), 'utf8');
console.log(`✅ JSON registry: .aios-core/data/custom-entity-registry.json`);
